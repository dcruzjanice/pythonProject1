#data types

a = "Hello World"
print(type(a))

b = 18
print(type(b))

c = 30.5
print(type(c))

d = 1j
print(type(d))

e = ["apple", "banana", "cherry"]
print(type(e))

f = ("apple", "banana", "cherry")
print(type(f))

g = range(8)
print(type(g))

h = {"name" : "Janice", "age" : 18}
print(type(h))

i = {"apple", "banana", "cherry"}
print(type(i))

j= frozenset({"apple", "banana", "cherry"})
print(type(j))

k = True
print(type(k))

l = b"Hi"
print(type(l))

m = bytearray(5)
print(type(m))

n = memoryview(bytes(5))
print(type(n))

o = None
print(type(o))
